#include "libft.h"

int		ft_atoi(const char *nb)
{
	long	a;
	int		i;

	a = 0;
	i = 1;
	while ((*nb && (*nb >= 9 && *nb <= 13)) || *nb == 32)
		nb++;
	if (*nb == '+' || *nb == '-')
		i = ((*nb++ == '-') ? -1 : 1);
	while (*nb)
	{
		if (*nb >= '0' && *nb <= '9')
		{
			if (a < 0)
				return (a);
			if (a > 922337293685477580 && i == 1)
				return (-1);
			if (a >= 922337203685477580 && *nb >= '8' && i == -1)
				return (0);
			a = (*nb++ - '0') + a * 10;
		}
		else
			return (a * i);
	}
	return (a * i);
}
