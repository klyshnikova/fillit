#include "libft.h"

char	*ft_strchr(const char *str, int ch)
{
	char	*s;
	int		i;

	s = (char *)str;
	i = 0;
	while (s[i] != ch)
	{
		if (s[i] == '\0')
			return (NULL);
		i++;
	}
	return (s + i);
}
